/**
 * Generate a basic entity class
 * 
 * @param {Object} params - Command parameters
 * @param {string} params.name - Entity name
 * @param {string} params.package - Java package
 * @param {string} params.tableName - Table name (optional)
 * @returns {string} Generated entity class
 */
exports.createEntity = function(params) {
    const { name, package: pkg, tableName } = params;
    const className = name;
    const javaTableName = tableName || className.toLowerCase();
    
    return `package ${pkg};

import org.norm4j.Table;
import org.norm4j.Id;
import org.norm4j.GeneratedValue;
import org.norm4j.GenerationType;
import org.norm4j.Column;

@Table(name = "${javaTableName}")
public class ${className} {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    @Column(nullable = false)
    private String name;

    // Getters and Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
`;
};

