/**
 * Generate DTO mapper
 * 
 * @param {Object} params - Command parameters
 * @param {string} params.entity - Entity class name
 * @param {string} params.dto - DTO class name
 * @param {Array} params.relations - Relationship mappings
 * @returns {string} DTO mapper code
 */
exports.generateMapper = function(params) {
    const { entity, dto, relations } = params;
    
    const relationMappings = relations.map(rel => 
        `    .join(${dto}::get${rel.target}, ${rel.entity}, ${rel.dto})`
    ).join('\n');
    
    return `RecordMapper<${entity}, ${dto}> mapper = RecordMapperBuilder.from(${entity}.class, ${dto}.class)
${relationMappings}
    .endJoin()
    .build(tableManager);

${dto} dto = mapper.map(entityInstance);
`;
};

